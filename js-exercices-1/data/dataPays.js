const createSpan = (content, classname) => {
  const span = document.createElement('span');
  span.textContent = content;
  span.className = classname;
  return span;
}
const createDivPays = pays => {
  const divPays = document.createElement('div');
  Object.keys(pays)
        .map( key => createSpan(pays[key], key))
        .map( span => divPays.appendChild(span));
  divPays.className = 'pays';
  return divPays;
}

const displayListePays = listePays => {
  const displayPays = document.getElementById('displaypays');
  displayPays.innerHTML = '';
  listePays.forEach( pays => displayPays.appendChild(createDivPays(pays) ) );
}
